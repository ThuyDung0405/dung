    public void setData(ArrayList al) {      //truyen vao ham nay 1 cai arraylist - chinh la listNhanVien o ham main
        DefaultTableModel model3 = new DefaultTableModel();
        model3.addColumn("Ho ten");
        model3.addColumn("Ngay sinh");
        model3.addColumn("Dia chi");
        model3.addColumn("Gioi tinh");
        model3.addColumn("Phong ban");
        model3.addColumn("He so luong");
        model3.addColumn("Tham nien");
        model3.addColumn("Luong co ban");
        model3.addColumn("Luong thuc linh");

        for (int i = 0; i < al.size(); i++) {
            NhanVien nv = (NhanVien) al.get(i);
            Float luongthuclinh = nv.LuongThucLinh();
            Object[] rowData = {nv.HoTen, nv.NgaySinh, nv.DiaChi, nv.GioiTinh, nv.getPhongBan(), nv.getHeSoLuong(), nv.getThamNien(), nv.getLuongCoBan(), luongthuclinh};
            model3.addRow(rowData);
        }

        tableListNV.setModel(model3);
    }

