    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Hàm này tạo sự kiện khi ấn nút Thêm nhân viên
        if (KiemTraRong() == true) {
            JOptionPane.showMessageDialog(null, "Chua du thong tin!!");
            return;
            
        }

        try {       //kiểm tra hệ số lương
            Float.parseFloat(tfHesoluong.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ban nhap sai He so luong!");
            return;
        }

        try {       //kiểm tra thâm niên
            Integer.parseInt(tfThamnien.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ban nhap sai Tham nien!");
            return;
        }

        try {       //kiểm tra lương cơ bản
            Float.parseFloat(tfLuongcoban.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ban nhap sai Luong co ban!");
            return;
        }
        Float hesoluong = Float.parseFloat(tfHesoluong.getText());
        int thamnien = Integer.parseInt(tfThamnien.getText());
        Float Luongcoban = Float.parseFloat(tfLuongcoban.getText());
        if (hesoluong <= 0 || thamnien < 0 || Luongcoban <= 0) {
            JOptionPane.showMessageDialog(null, "de nghe kiem tra lai cac so, khong duoc nhap so am!");
            return;
        }
        Nhanvien nv = new Nhanvien(tfHoten.getText(), tfNgaysinh.getText(), tfDiachi.getText(),
                nam.isSelected() ? "nam" : "nu", cbb.getSelectedItem().toString(),
                Float.parseFloat(tfHesoluong.getText()), Integer.parseInt(tfThamnien.getText()),
                Float.parseFloat(tfLuongcoban.getText()));
        nv.HienThi();
        JOptionPane.showMessageDialog(null, "Them thanh cong");
        DSnhanvien.add(nv);
        tfHoten.setText("");
        tfNgaysinh.setText("");
        tfDiachi.setText("");
        tfHesoluong.setText("");
        tfThamnien.setText("");
        tfLuongcoban.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //hàm này tạo sự kiện khi ấn nút Lưu dữ liệu
        try {
            FileWriter fw = new FileWriter("nhanvien.dat", true);
            BufferedWriter bw = new BufferedWriter(fw);
            for (int i = 0; i < DSnhanvien.size(); i++) {
                Nhanvien nvv = (Nhanvien) DSnhanvien.get(i);
                bw.append(nvv.HoTen + "$" + nvv.NgaySinh + "$" + nvv.DiaChi + "$" + nvv.GioiTinh + "$"
                        + nvv.getPhongBan() + "$" + nvv.getHeSoLuong() + "$" + nvv.getThamNien() + "$"
                        + nvv.getLuongCoBan());
                bw.newLine();
            }

            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(null, "luu file thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Luu file that bai!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

