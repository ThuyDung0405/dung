    public boolean KiemTraRong() {
        if (tfHoten.getText().equals("") || tfNgaysinh.getText().equals("") || tfDiachi.getText().equals("")
                || tfHesoluong.getText().equals("") || tfLuongcoban.getText().equals("")
                || tfThamnien.getText().equals("") || (!nam.isSelected() && !nu.isSelected())) {
            return true;    //nếu 1 trong số những cái trên rỗng thì kết thúc hàm, trả về giá trị TRUE 
        }
        return false;       //ngược lại...
    }

