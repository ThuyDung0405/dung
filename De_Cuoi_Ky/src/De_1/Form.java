    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (kiemTraRong() == true) {
            JOptionPane.showMessageDialog(null, "ban chua nhap du thong tin ");
            return;
        }
        try {
            FileWriter fw = new FileWriter("sinhvien.dat", true);
            BufferedWriter bw = new BufferedWriter(fw);
            Student st = new Student(txtHoten.getText(), txtNgaysinh.getText(), txtDiachi.getText(),
                                     btnNam.isSelected() ? "Nam" : "Nữ", txtMaSV.getText(), txtEmail.getText(), Float.parseFloat(txtDiemTK.getText()));
            bw.append(st.hoTen+"$"+st.ngaySinh+"$"+st.diaChi+"$"+st.gioiTinh+"$"+st.getMaSV()+"$"+st.getEmail()+"$"+st.getDiemTK());
            bw.newLine();
            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(null, "Luu thanh cong");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Luu that bai");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

