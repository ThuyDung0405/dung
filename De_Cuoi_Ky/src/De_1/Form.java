    public boolean kiemTraRong() {
        if (txtHoten.getText().equals("") || txtNgaysinh.getText().equals("") || txtDiachi.getText().equals("")
                || txtMaSV.getText().equals("") || txtEmail.getText().equals("") || txtDiemTK.getText().equals("")
                || (!btnNam.isSelected() && !btnNu.isSelected())) {
            return true;
        }
        return false;
    }

