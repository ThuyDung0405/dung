    public boolean kiemTraRongSVAT() {
        if (txtMaSV.getText().equals("") || txtHoten.getText().equals("") || txtNgaySinh.getText().equals("")
                || txtDiemTB.getText().equals("") || txtHocphi.getText().equals("") || (!btnNam.isSelected() && !btnNu.isSelected())) {
            return true;
        }
        return false;
    }

    public boolean kiemTraRongSVMM() {
        if (txtMaSV.getText().equals("") || txtHoten.getText().equals("") || txtNgaySinh.getText().equals("")
                || txtDiemTB.getText().equals("") || txtDonVi.getText().equals("") || txtLuong.getText().equals("") || (!btnNam.isSelected() && !btnNu.isSelected())) {
            return true;
        }
        return false;
    }

