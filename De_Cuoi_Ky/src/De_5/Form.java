    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (!btnSVAT.isSelected() && !btnSVMM.isSelected()) {
            JOptionPane.showMessageDialog(null, "Ban phai chon loai SV");
        } else {
            if (btnSVAT.isSelected()) {
                if (kiemTraRongSVAT() == false) {
                    try {
                        SinhVienATTT at = new SinhVienATTT(txtMaSV.getText(),txtHoten.getText(),txtNgaySinh.getText(),
                                                           btnNam.isSelected()?"nam":"nu",Float.parseFloat(txtDiemTB.getText()),Integer.parseInt(txtHocphi.getText()));
                        FileWriter fw = new FileWriter("svat.dat", true);

                        BufferedWriter bw = new BufferedWriter(fw);
                        
                        bw.append(at.maSV+"$"+at.hoTen+"$"+at.ngaySinh+"$"+at.gioiTinh+"$"+at.diemTB+"$"+at.getHocPhi());
                        bw.newLine();
                        bw.close();
                        fw.close();
                        JOptionPane.showMessageDialog(null, "Luu file SVAT thanh cong");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Luu file that bai");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ban chua nhap du thong tin");
                    return;
                }
            } else {
                if (kiemTraRongSVMM() == false) {
                    //thuc hien luu file
                    try {
                        SinhVienMM mm = new SinhVienMM(txtMaSV.getText(),txtHoten.getText(),txtNgaySinh.getText(),
                                                           btnNam.isSelected()?"nam":"nu",Float.parseFloat(txtDiemTB.getText()),txtDonVi.getText(),Float.parseFloat(txtLuong.getText()));
                        FileWriter fw = new FileWriter("svmm.dat", true);

                        BufferedWriter bw = new BufferedWriter(fw);
                        
                        bw.append(mm.maSV+"$"+mm.hoTen+"$"+mm.ngaySinh+"$"+mm.gioiTinh+"$"+mm.diemTB+"$"+mm.getDonVi()+"$"+mm.getLuong());
                        bw.newLine();
                        bw.close();
                        fw.close();
                        JOptionPane.showMessageDialog(null, "Luu file SVMM thanh cong");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Luu file that bai");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Ban phai nhap du thong tin");
                    return ;
                }
            }
        }

    }//GEN-LAST:event_btnLuuActionPerformed

